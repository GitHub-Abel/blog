package com.windhc.domain;

import java.util.List;
import com.windhc.config.Constants;

/**
 * Created by HC on 2016/6/18.
 */
public class Article extends BaseEntity {

    private String title;

    private String summary;

    private String content;

    private Constants.ArticleStatus status;

    private Long categoryId;

    private Long userId;

    private boolean deleted = false;

    private Category category;
    private User user;
    private List<Comment> comments;

    public Constants.ArticleStatus getStatus() {
        return status;
    }

    public void setStatus(Constants.ArticleStatus status) {
        this.status = status;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSummary() {
        return summary;
    }

    public void setSummary(String summary) {
        this.summary = summary;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Long getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(Long categoryId) {
        this.categoryId = categoryId;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public boolean isDeleted() {
        return deleted;
    }

    public void setDeleted(boolean deleted) {
        this.deleted = deleted;
    }

    public Category getCategory() {
        return category;
    }

    public void setCategory(Category category) {
        this.category = category;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public List<Comment> getComments() {
        return comments;
    }

    public void setComments(List<Comment> comments) {
        this.comments = comments;
    }
}
